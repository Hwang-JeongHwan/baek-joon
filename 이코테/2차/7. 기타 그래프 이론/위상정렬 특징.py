# ex) 선수과목을 고려한 학습 순서 설정

# 자료구조 => 알고리즘 => 고급 알고리즘 
# 자료구조 => 고급 알고리즘 <= 알고리즘
# 고급 알고리즘을 수강하기 위해서는 자료구조와 알고리즘을 모두 수강해야함

# 고급 알고리즘은 들어오는 간선을 갖고 있음


